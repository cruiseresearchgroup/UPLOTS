class Dataset_NPY(Dataset):
    """Dataset for .npy files with shape (N, T, C) or (N, T).
    Each sample i returns:
      seq_x = data[i, :seq_len, :]
      seq_y = data[i, seq_len-label_len : seq_len+pred_len, :]
    Optional aug_data_path appends synthetic samples to training split only.
    """

    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='data.npy',
                 target='OT', scale=True, timeenc=0, freq='h',
                 aug_data_path=None, train_ratio=1.0):
        if size is None:
            self.seq_len = 12
            self.label_len = 4
            self.pred_len = 12
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]

        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.scale = scale
        self.root_path = root_path
        self.data_path = data_path
        self.aug_data_path = aug_data_path or ''
        self.train_ratio = train_ratio
        self.__read_data__()

    def __read_data__(self):
        raw = np.load(os.path.join(self.root_path, self.data_path))
        if raw.ndim == 2:
            raw = raw[:, :, np.newaxis]
        N, T, C = raw.shape

        n_train = int(N * 0.7)
        n_val = int(N * 0.2)

        if self.scale:
            self.scaler = StandardScaler()
            self.scaler.fit(raw[:n_train].reshape(-1, C))
            raw = self.scaler.transform(raw.reshape(-1, C)).reshape(N, T, C)

        if self.set_type == 0:   # train
            train_data = raw[:n_train].copy()
            # subsample original train data
            if self.train_ratio < 1.0:
                n_use = max(1, int(len(train_data) * self.train_ratio))
                train_data = train_data[:n_use]
            self.data = train_data
            if self.aug_data_path:
                aug = np.load(os.path.join(self.root_path, self.aug_data_path))
                if aug.ndim == 2:
                    aug = aug[:, :, np.newaxis]
                if self.scale:
                    aug = self.scaler.transform(aug.reshape(-1, C)).reshape(-1, T, C)
                self.data = np.concatenate([self.data, aug], axis=0)
        elif self.set_type == 1:  # val
            self.data = raw[n_train: n_train + n_val].copy()
        else:                     # test
            self.data = raw[n_train + n_val:].copy()

    def __getitem__(self, index):
        sample = self.data[index]
        seq_x = sample[:self.seq_len]
        r_beg = self.seq_len - self.label_len
        seq_y = sample[r_beg: r_beg + self.label_len + self.pred_len]

        seq_x_mark = np.zeros((self.seq_len, 4), dtype=np.float32)
        seq_y_mark = np.zeros((self.label_len + self.pred_len, 4), dtype=np.float32)
        return seq_x.astype(np.float32), seq_y.astype(np.float32), seq_x_mark, seq_y_mark

    def __len__(self):
        return len(self.data)

    def inverse_transform(self, data):
        if self.scale:
            return self.scaler.inverse_transform(data)
        return data


